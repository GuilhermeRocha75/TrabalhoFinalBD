    private void btnEnviarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEnviarActionPerformed
      // Captura os valores da tela
    String data = txtData.getText();
    String hora = txtHora.getText();
    String descricao = txtDescricao.getText();
    int clienteId = Integer.parseInt(txtCliente.getText().trim()); // Cliente ID

    // Valida os campos antes de prosseguir
    if (data.isEmpty() || hora.isEmpty() || descricao.isEmpty() || clienteId == 0) {
        JOptionPane.showMessageDialog(null, "Por favor, preencha todos os campos!");
        return;
    }

    try {
        // Definindo o formatador de data e hora
        DateTimeFormatter dateFormatter = DateTimeFormatter.ofPattern("dd/MM/yyyy");
        DateTimeFormatter timeFormatter = DateTimeFormatter.ofPattern("HH:mm");

        // Convertendo a String para LocalDate e LocalTime usando os formatadores
        LocalDate dataFormatada = LocalDate.parse(data, dateFormatter);
        LocalTime horaFormatada = LocalTime.parse(hora, timeFormatter);

        // Cria um objeto AgendaDTO com os dados capturados e formatados
        AgendaDTO agenda = new AgendaDTO();
        agenda.setData(dataFormatada);  // Define a data formatada
        agenda.setHora(horaFormatada);  // Define a hora formatada
        agenda.setDescricao(descricao);
        agenda.setClienteId(clienteId);

        // Chama o DAO para inserir o compromisso
        AgendaDAO dao = new AgendaDAO();
        dao.adicionarCompromisso(agenda);

        // Atualiza a tabela com os compromissos (implemente o método de listar compromissos)
        atualizarTabela();

       

    } catch (DateTimeParseException e) {
        // Tratamento para erros de formatação de data ou hora
        JOptionPane.showMessageDialog(null, "Erro no formato da data ou hora. Use o formato dd/MM/yyyy para data e HH:mm para hora.");
    } catch (NumberFormatException e) {
        // Tratamento para erro de conversão do ID do cliente
        JOptionPane.showMessageDialog(null, "Erro no formato do ID do cliente. Certifique-se de que é um número válido.");
    } catch (Exception e) {
        // Tratamento genérico para outros erros
        JOptionPane.showMessageDialog(null, "Erro ao adicionar compromisso: " + e.getMessage());
    }
    }//GEN-LAST:event_btnEnviarActionPerformed

