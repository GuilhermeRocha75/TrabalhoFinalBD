  private void atualizarTabela() {
    try {
        // Usa o DAO para buscar os compromissos
        AgendaDAO dao = new AgendaDAO();
        List<AgendaDTO> compromissos = dao.listarCompromissos();

        // Obtém o modelo da tabela e reseta os dados atuais
        DefaultTableModel model = (DefaultTableModel) tabela.getModel();
        model.setRowCount(0);  // Limpa a tabela

        // Percorre a lista de compromissos e adiciona cada um na tabela
        for (AgendaDTO agenda : compromissos) {
            model.addRow(new Object[]{
                agenda.getData(),
                agenda.getHora(),
                agenda.getDescricao(),
                agenda.getClienteId()
            });
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Erro ao atualizar tabela: " + e);
    }
}



    

